/** Find a single vehicle by slug */
export function getVehicleBySlug(slug: string): Vehicle | undefined {
  return vehicles.find((v) => v.slug === slug);
}

/** Get all available vehicles */
export function getAvailableVehicles(): Vehicle[] {
  return vehicles.filter((v) => v.status === "AVAILABLE");
}

/** Get featured vehicles (first 9 for homepage) */
export function getFeaturedVehicles(): Vehicle[] {
  return vehicles.filter((v) => v.status === "AVAILABLE").slice(0, 9);
}

/** Get unique makes from inventory */
export function getUniqueMakes(): string[] {
  return [...new Set(vehicles.map((v) => v.make))].sort();
}

/** Get vehicles similar to a given one (same body type, different id) */
export function getSimilarVehicles(
  vehicleId: string,
  limit = 4
): Vehicle[] {
  const target = vehicles.find((v) => v.id === vehicleId);
  if (!target) return vehicles.slice(0, limit);
  return vehicles
    .filter((v) => v.id !== vehicleId && v.bodyType === target.bodyType)
    .slice(0, limit);
}
